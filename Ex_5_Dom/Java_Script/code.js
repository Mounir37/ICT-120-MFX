document.addEventListener("DOMContentLoaded", evenement);

function div1Click() {
  L1.classList.add("L1")
}

function div2Click() {
    L1.classList.remove("L1")
}

function div3Survol() {
    Arial.classList.add("Arial")
}

function div3SurvolRien() {
    Arial.classList.remove("Arial")
}

